class Fruit:
    is_acidic = False
    def __init__(self,name,color,taste):
        self.name = name
        self.color = color
        self.taste = taste
def eat(self):
    return f"{self.name} is rare to find in {self.color} and has a {self.taste}"
def peel(self):
    return f"She is peeling a {self.name} which {self.is_acidic}"
def cut(self):
    return f"I cut {self.name} into four pieces"